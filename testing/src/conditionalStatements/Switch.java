package conditionalStatements;

public class Switch {
public static void main(String[] args) {
	int num=5;
	switch(num) {
	case 1:
		System.out.println("today is mpnday");
	    break;
	case 2:
		System.out.println("today is tuesday");
	    break;
	case 3:
		System.out.println("today is sunday");
		break;
		default:
			System.out.println("enter correct input from 1 to 3");
	
	}
}
}
