package operators;

public class relationalOperators {
	public static void main(String[]args) {
	//variable declaration
	int a=25, b=38;
	// value of a and b 
	System.out.println("a is"+a + "b is"+b);
	// == operator
	System.out.println(a==b);
	// != operator
	System.out.println(a!=b);
	// > operator
	System.out.println(a>b);
	// < operator
	System.out.println(a<b);
	// >= operator
	System.out.println(a>=b);
	// <= operator
	System.out.println(a<=b);
	}
	//relational operator result shown in boolean form.
}
