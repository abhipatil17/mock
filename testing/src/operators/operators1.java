package operators;

public class operators1 {
public static void main (String[]args) {
	int a=50, b=3, c=14;
	System.out.println("a+b+c=" + (a+b+c));
	System.out.println("a+b-c=" + (a+b-c));
	System.out.println("a*b+c=" + (a*b+c));
	System.out.println("a/b*c+a=" + (a/b*c+a));
	System.out.println("a/b%c=" + (a/b%c));
	
}
}
