package operators;

public class incrementAndDecrementOperator1 {
public static void main(String[]args) {
	int a=3, b=105;
	int report2, report3;
	System.out.println("value of a="+ a);
	report2=++a;
	System.out.println("after increment="+report2);
	report2=++a;
	System.out.println("after increment="+report2);
	System.out.println("value of b="+ b);
	report3=--b;
	System.out.println("after decrement="+report3);
	report3=--b;
	System.out.println("after decrement="+report3);
	
}
}
